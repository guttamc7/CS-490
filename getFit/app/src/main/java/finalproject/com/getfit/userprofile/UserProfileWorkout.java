public class UserProfileWorkout
{
    private String userProfileWorkoutName;
    private String userProfileWorkoutId;
    private String userProfileUserName;
    private String userProfileWorkoutDescription;
    private String userProfileWorkoutLevel;

    public String getUserProfileWorkoutName() {
        return userProfileWorkoutName;
    }

    public void setUserProfileWorkoutName(String userProfileWorkoutName) {
        this.userProfileWorkoutName = userProfileWorkoutName;
    }

    public String getUserProfileWorkoutId() {
        return userProfileWorkoutId;
    }

    public void setUserProfileWorkoutId(String userProfileWorkoutId) {
        this.userProfileWorkoutId = userProfileWorkoutId;
    }

    public String getUserProfileUserName() {
        return userProfileUserName;
    }

    public void setUserProfileUserName(String userProfileUserName) {
        this.userProfileUserName = userProfileUserName;
    }

    public String getUserProfileWorkoutDescription() {
        return userProfileWorkoutDescription;
    }

    public void setUserProfileWorkoutDescription(String userProfileWorkoutDescription) {
        this.userProfileWorkoutDescription = userProfileWorkoutDescription;
    }

    public String getUserProfileWorkoutLevel() {
        return userProfileWorkoutLevel;
    }

    public void setUserProfileWorkoutLevel(String userProfileWorkoutLevel) {
        this.userProfileWorkoutLevel = userProfileWorkoutLevel;
    }


}
