public class TrendingWorkout
{
    private String trendingWorkoutName;
    private String trendingWorkoutId;
    private String trendingWorkoutUserName;
    private String trendingWorkoutDescription;
    private String trendingWorkoutLevel;

    public String getTrendingWorkoutLevel()
    {
        return trendingWorkoutLevel;
    }

    public void setTrendingWorkoutLevel(String trendingWorkoutLevel)
    {
        this.trendingWorkoutLevel = trendingWorkoutLevel;
    }

    public String getTrendingWorkoutName()
    {
        return trendingWorkoutName;
    }

    public void setTrendingWorkoutName(String trendingWorkoutName)
    {
        this.trendingWorkoutName = trendingWorkoutName;
    }

    public String getTrendingWorkoutId()
    {
        return trendingWorkoutId;
    }

    public void setTrendingWorkoutId(String trendingWorkoutId)
    {
        this.trendingWorkoutId = trendingWorkoutId;
    }

    public String getTrendingWorkoutUserName()
    {
        return trendingWorkoutUserName;
    }

    public void setTrendingWorkoutUserName(String trendingWorkoutUserName)
    {
        this.trendingWorkoutUserName = trendingWorkoutUserName;
    }

    public String getTrendingWorkoutDescription()
    {

        return trendingWorkoutDescription;
    }

    public void setTrendingWorkoutDescription(String trendingWorkoutDescription)
    {
        this.trendingWorkoutDescription = trendingWorkoutDescription;
    }

}
